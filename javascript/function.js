//Function Declaration

function greet(name) {
  console.log(`Hello ${name}!`);
}

//Function Expression

const greet = function (name) {
  console.log(`Hello ${name}!`);
};

//Arrow Function

const greet = (name) => {
  console.log(`Hello ${name}!`);
};

//Immediately Invoked Function Expression (IIFE)

(function () {
  console.log("Hello World!");
})();





